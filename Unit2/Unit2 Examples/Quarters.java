public class Quarters
{
    public static void main(String[] args)
    {
        System.out.println("Aneesh LLC");
        System.out.println("========================");
        
        int quarter1=54, quarter2=55, quarter3=56, quarter4=57;

        System.out.println("The income for the first quarter is $" + quarter1 + ".");
        System.out.println("The income for the second quarter is $" + quarter2 + ".");
        System.out.println("The income for the third quarter is $" + quarter3 + ".");
        System.out.println("The income for the fourth quarter is $" + quarter4 + ".");
        
        System.out.println("The total annual income $" + (quarter1 + quarter2 + quarter3 + quarter4) + ".");
    }
}